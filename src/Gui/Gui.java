    private void b_nguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_nguardarActionPerformed
        try {
            int serie = Integer.parseInt(t_serie.getText());
            String marca = t_marca.getText();
            double precio = 0;
            try {
                precio = Double.parseDouble(t_precio.getText());
                if (precio < 1) {
                    throw new EmptyStackException();
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(vagregar, "Formato de precio invalido", "ERROR", 2);
            }
            String ubicacion = (String) t_ubicacion.getSelectedItem();
            if (titulo.getText().equals("Nueva Llanta")) {
                int num_llanta = 0;
                num_llanta = (int) t_numllanta.getValue();
                if (num_llanta < 1) {
                    JOptionPane.showMessageDialog(vagregar, "Numero de llanta invalido", "ERROR", 2);
                    throw new EmptyStackException();
                }
                int num_malla = 0;
                num_malla = (int) t_nummalla.getValue();
                if (num_malla < 1) {
                    JOptionPane.showMessageDialog(vagregar, "Numero de malla invalido", "ERROR", 2);
                    throw new EmptyStackException();
                }
                inventario.add(new Llantas(num_llanta, num_malla, serie, marca, precio, ubicacion));
                t_numllanta.setValue(1);
                t_nummalla.setValue(1);
            } else if (titulo.getText().equals("Nuevo Repuesto")) {
                String nombre = t_nombre.getText();
                String Modelo = t_modelo.getText();
                String year = t_ano.getText();
                boolean usado = false;
                if (r_nuevo.isSelected()) {
                    usado = false;
                } else {
                    usado = true;
                }
                inventario.add(new Repuesto(nombre, Modelo, year, usado, serie, marca, precio, ubicacion));
                t_nombre.setText("");
                t_modelo.setText("");
                t_ano.setText("");
                r_nuevo.setSelected(true);
            } else {
                String material = t_material.getText();
                String diseño = t_diseño.getText();
                String color = t_color.getText();
                int num_rin = 0;

                num_rin = (int) t_numrin.getValue();
                if (num_rin < 1) {
                    JOptionPane.showMessageDialog(vagregar, "Numero de rin invalido", "ERROR", 2);
                    throw new EmptyStackException();
                }

                inventario.add(new Rines(num_rin, material, diseño, serie, marca, precio, ubicacion));
                t_diseño.setText("");
                t_material.setText("");
                t_color.setText("");
                t_numrin.setValue(1);
            }
            t_marca.setText("");
            t_precio.setText("");
            t_ubicacion.setSelectedIndex(0);
            vagregar.setVisible(false);
            DefaultTableModel m = new DefaultTableModel();
            m.addColumn("Serie");
            m.addColumn("Marca");
            m.addColumn("Precio");
            String temporal[] = new String[4];
            for (Inventario temp : inventario) {
                temporal[0] = temp.getSerie() + "";
                temporal[1] = temp.getMarca();
                temporal[2] = temp.getPrecio() + "";
                m.addRow(temporal);
            }
            table.setModel(m);
        } catch (Exception e) {

        }
    }//GEN-LAST:event_b_nguardarActionPerformed

