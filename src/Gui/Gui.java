    private void formWindowClosing(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowClosing
        File archivo = null;
        try {
            archivo = new File("./Inventario.chun");
            FileOutputStream salida = new FileOutputStream(archivo);
            ObjectOutputStream objeto = new ObjectOutputStream(salida);
            for (int i = 0; i < inventario.size(); i++) {
                objeto.writeObject(inventario.get(i));
                objeto.flush();
            }
            objeto.close();
            salida.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_formWindowClosing

    private void detalleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_detalleActionPerformed
        Inventario temp = null;
        int serie = Integer.parseInt((String) table.getValueAt(table.getSelectedRow(), 0));
        for (Inventario temporal : inventario) {
            if (temporal.getSerie() == serie) {
                temp = temporal;
            }
        }
        seried.setText(temp.getSerie() + "");
        marcad.setText(temp.getMarca());
        ubicaciond.setText(temp.getUbicacion());
        preciod.setText(temp.getPrecio() + "");

        if (temp instanceof Llantas) {
            titulazo.setText("Llanta");
            num_llantad.setText(((Llantas) temp).getNum_llanta() + "");
            num_mallasd.setText(((Llantas) temp).getNum_malla() + "");
            pRepuesto.setVisible(false);
            pLlanta.setVisible(true);
            pRines.setVisible(false);
        }
        if (temp instanceof Repuesto) {
            titulazo.setText("Repuesto");
            nombred.setText(((Repuesto) temp).getNombre());
            modelod.setText(((Repuesto) temp).getModelo());
            anod.setText(((Repuesto) temp).getYear());
            if (((Repuesto) temp).isUsado()) {
                estadod.setText("Usado");
            } else {
                estadod.setText("Nuevo");
            }
            pLlanta.setVisible(false);
            pRepuesto.setVisible(true);
            pRines.setVisible(false);

        } else if (temp instanceof Rines) {
            titulazo.setText("Rines");
            pRines.setVisible(true);
            pRepuesto.setVisible(false);
            pLlanta.setVisible(false);
            num_rind.setText(((Rines) temp).getNum_rin() + "");
            materiald.setText(((Rines) temp).getMaterial());
            disenod.setText(((Rines) temp).getDiseño());
            colord.setText(((Rines) temp).getColor());
        }
        vdetalle.pack();
        vdetalle.setModal(true);
        vdetalle.setLocationRelativeTo(this);
        vdetalle.setVisible(true);
    }//GEN-LAST:event_detalleActionPerformed

