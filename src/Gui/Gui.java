    public void cargar() {
        File archivo = null;
        try {
            archivo = new File("./Inventario.chun");
            if (archivo.exists()) {
                FileInputStream entrada = new FileInputStream(archivo);
                ObjectInputStream objeto = new ObjectInputStream(entrada);
                Inventario temp;
                try {
                    while ((temp = (Inventario) objeto.readObject()) != null) {
                        inventario.add(temp);
                    }
                } catch (EOFException e) {
                } finally {
                    objeto.close();
                    entrada.close();
                }
            } else {
                FileOutputStream salida = new FileOutputStream(archivo);
                salida.close();
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }


